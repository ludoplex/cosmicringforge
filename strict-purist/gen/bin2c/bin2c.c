/* MBSE Stacks — Binary to C Embedder
 * Ring 0: Pure C, minimal bootstrap
 *
 * Embeds binary files as C arrays for static linking.
 * Generates header with declarations and source with data.
 *
 * Usage: bin2c <manifest.txt> [output_dir] [prefix]
 *
 * Manifest format (one per line):
 *   <symbol_name> <file_path>
 *
 * Example:
 *   logo_png assets/logo.png
 *   font_ttf assets/font.ttf
 */

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <ctype.h>
#include <stdint.h>
#include <time.h>

#define BIN2C_VERSION "1.0.0"
#define MAX_LINE 1024
#define MAX_ENTRIES 256
#define MAX_NAME 64
#define MAX_PATH 512

typedef struct {
    char symbol[MAX_NAME];
    char path[MAX_PATH];
    size_t size;
} entry_t;

static entry_t entries[MAX_ENTRIES];
static int entry_count = 0;

/* ── Utilities ────────────────────────────────────────────────────── */

static void trim(char *s) {
    char *start = s;
    while (*start && isspace((unsigned char)*start)) start++;
    if (start != s) memmove(s, start, strlen(start) + 1);
    char *end = s + strlen(s) - 1;
    while (end > s && isspace((unsigned char)*end)) *end-- = '\0';
}

static void to_upper(char *s) {
    for (; *s; s++) *s = (char)toupper((unsigned char)*s);
}

static int is_valid_symbol(const char *s) {
    if (!s || !*s) return 0;
    if (!isalpha((unsigned char)*s) && *s != '_') return 0;
    for (const char *p = s + 1; *p; p++) {
        if (!isalnum((unsigned char)*p) && *p != '_') return 0;
    }
    return 1;
}

/* ── Manifest Parser ──────────────────────────────────────────────── */

static int parse_manifest(const char *filename) {
    FILE *f = fopen(filename, "r");
    if (!f) {
        fprintf(stderr, "Error: Cannot open manifest %s\n", filename);
        return -1;
    }

    char line[MAX_LINE];
    while (fgets(line, sizeof(line), f)) {
        trim(line);
        if (line[0] == '\0' || line[0] == '#') continue;

        if (entry_count >= MAX_ENTRIES) {
            fprintf(stderr, "Error: Too many entries\n");
            fclose(f);
            return -1;
        }

        entry_t *e = &entries[entry_count];
        if (sscanf(line, "%63s %511s", e->symbol, e->path) != 2) {
            fprintf(stderr, "Error: Invalid manifest line: %s\n", line);
            continue;
        }

        if (!is_valid_symbol(e->symbol)) {
            fprintf(stderr, "Error: Invalid symbol name: %s\n", e->symbol);
            continue;
        }

        /* Get file size */
        FILE *bf = fopen(e->path, "rb");
        if (!bf) {
            fprintf(stderr, "Warning: Cannot open %s, skipping\n", e->path);
            continue;
        }
        fseek(bf, 0, SEEK_END);
        e->size = (size_t)ftell(bf);
        fclose(bf);

        entry_count++;
    }

    fclose(f);
    return 0;
}

/* ── Code Generation ──────────────────────────────────────────────── */

static void generate_header(FILE *out, const char *guard) {
    fprintf(out, "/* AUTO-GENERATED by bin2c %s — DO NOT EDIT */\n", BIN2C_VERSION);
    fprintf(out, "#ifndef %s\n", guard);
    fprintf(out, "#define %s\n\n", guard);
    fprintf(out, "#include <stddef.h>\n\n");
}

static int generate_resources_h(const char *outdir, const char *prefix) {
    char path[MAX_PATH];
    char guard[128];
    char header_name[128];

    snprintf(header_name, sizeof(header_name), "%s_resources.h", prefix);
    for (char *p = header_name; *p; p++) *p = (char)tolower((unsigned char)*p);

    snprintf(guard, sizeof(guard), "%s_RESOURCES_H", prefix);
    to_upper(guard);

    snprintf(path, sizeof(path), "%s/%s", outdir, header_name);
    FILE *out = fopen(path, "w");
    if (!out) {
        fprintf(stderr, "Error: Cannot create %s\n", path);
        return -1;
    }

    generate_header(out, guard);

    for (int i = 0; i < entry_count; i++) {
        entry_t *e = &entries[i];
        fprintf(out, "/* %s (%zu bytes) */\n", e->path, e->size);
        fprintf(out, "extern const unsigned char %s[];\n", e->symbol);
        fprintf(out, "extern const size_t %s_size;\n", e->symbol);
        fprintf(out, "\n");
    }

    fprintf(out, "#endif /* %s */\n", guard);
    fclose(out);

    fprintf(stderr, "Generated %s\n", path);
    return 0;
}

static int generate_resources_c(const char *outdir, const char *prefix) {
    char path[MAX_PATH];
    char header_name[128];
    char impl_name[128];

    snprintf(header_name, sizeof(header_name), "%s_resources.h", prefix);
    for (char *p = header_name; *p; p++) *p = (char)tolower((unsigned char)*p);

    snprintf(impl_name, sizeof(impl_name), "%s_resources.c", prefix);
    for (char *p = impl_name; *p; p++) *p = (char)tolower((unsigned char)*p);

    snprintf(path, sizeof(path), "%s/%s", outdir, impl_name);
    FILE *out = fopen(path, "w");
    if (!out) {
        fprintf(stderr, "Error: Cannot create %s\n", path);
        return -1;
    }

    fprintf(out, "/* AUTO-GENERATED by bin2c %s — DO NOT EDIT */\n\n", BIN2C_VERSION);
    fprintf(out, "#include \"%s\"\n\n", header_name);

    for (int i = 0; i < entry_count; i++) {
        entry_t *e = &entries[i];

        FILE *bf = fopen(e->path, "rb");
        if (!bf) {
            fprintf(stderr, "Error: Cannot read %s\n", e->path);
            fclose(out);
            return -1;
        }

        fprintf(out, "/* %s */\n", e->path);
        fprintf(out, "const unsigned char %s[] = {\n", e->symbol);

        unsigned char buf[16];
        size_t total = 0;
        size_t n;
        while ((n = fread(buf, 1, sizeof(buf), bf)) > 0) {
            fprintf(out, "    ");
            for (size_t j = 0; j < n; j++) {
                fprintf(out, "0x%02x", buf[j]);
                if (total + j + 1 < e->size) fprintf(out, ",");
                if (j < n - 1) fprintf(out, " ");
            }
            fprintf(out, "\n");
            total += n;
        }

        fprintf(out, "};\n");
        fprintf(out, "const size_t %s_size = %zu;\n\n", e->symbol, e->size);

        fclose(bf);
    }

    fclose(out);
    fprintf(stderr, "Generated %s\n", path);
    return 0;
}

static void generate_version(const char *outdir, const char *profile) {
    char path[MAX_PATH];
    snprintf(path, sizeof(path), "%s/GENERATOR_VERSION", outdir);

    FILE *out = fopen(path, "w");
    if (!out) return;

    time_t now = time(NULL);
    struct tm *t = gmtime(&now);
    fprintf(out, "bin2c %s\n", BIN2C_VERSION);
    fprintf(out, "generated: %04d-%02d-%02dT%02d:%02d:%02dZ\n",
            t->tm_year + 1900, t->tm_mon + 1, t->tm_mday,
            t->tm_hour, t->tm_min, t->tm_sec);
    fprintf(out, "profile: %s\n", profile);
    fprintf(out, "entries: %d\n", entry_count);

    fclose(out);
}

/* ── Main ─────────────────────────────────────────────────────────── */

static void print_usage(void) {
    fprintf(stderr, "bin2c %s — Binary Resource Embedder\n", BIN2C_VERSION);
    fprintf(stderr, "\n");
    fprintf(stderr, "Usage: bin2c <manifest.txt> [output_dir] [prefix]\n");
    fprintf(stderr, "\n");
    fprintf(stderr, "Manifest format (one per line):\n");
    fprintf(stderr, "  <symbol_name> <file_path>\n");
    fprintf(stderr, "\n");
    fprintf(stderr, "Example manifest:\n");
    fprintf(stderr, "  logo_png assets/logo.png\n");
    fprintf(stderr, "  font_ttf assets/font.ttf\n");
    fprintf(stderr, "\n");
    fprintf(stderr, "Output:\n");
    fprintf(stderr, "  <prefix>_resources.h  — Declarations\n");
    fprintf(stderr, "  <prefix>_resources.c  — Data arrays\n");
}

int main(int argc, char *argv[]) {
    if (argc < 2) {
        print_usage();
        return 1;
    }

    const char *manifest = argv[1];
    const char *outdir = argc > 2 ? argv[2] : ".";
    const char *prefix = argc > 3 ? argv[3] : "MBSE";
    const char *profile = getenv("PROFILE");
    if (!profile) profile = "portable";

    if (parse_manifest(manifest) != 0) {
        return 1;
    }

    fprintf(stderr, "Parsed %d entries from %s\n", entry_count, manifest);

    if (entry_count == 0) {
        fprintf(stderr, "Warning: No entries to embed\n");
        return 0;
    }

    char cmd[MAX_PATH];
    snprintf(cmd, sizeof(cmd), "mkdir -p %s", outdir);
    system(cmd);

    if (generate_resources_h(outdir, prefix) != 0) return 1;
    if (generate_resources_c(outdir, prefix) != 0) return 1;

    generate_version(outdir, profile);

    return 0;
}
